# 0 ~ 9 까지 번호의 손가락
finger = [0] * 8
typing = { '1':0, 'q':0, 'a':0, 'z':0,
           '2': 1, 'w': 1, 's': 1, 'x': 1,
           '3': 2, 'e': 2, 'd': 2, 'c': 2,
           '4': 3, 'r': 3, 'f': 3, 'v': 3,
           '5': 3, 't': 3, 'g': 3, 'b': 3,

           '6': 4, 'y': 4, 'h': 4, 'n': 4,
           '7': 4, 'u': 4, 'j': 4, 'm': 4,
           '8': 5, 'i': 5, 'k': 5, ',': 5,
           '9': 6, 'o': 6, 'l': 6, '.': 6,
           '0': 7, 'p': 7, ';': 7, '/': 7,
           '-': 7, '[': 7, "'": 7, '=': 7, ']': 7
           }
word = input()
for w in word:
    finger[ typing[w] ] += 1
for i in finger:
    print(i, end = ' ')